    private void AturJTable(JTable Lihat, int Lebar[]){
        try{
            Lihat.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
            int banyak = Lihat.getColumnCount();
            for (int i = 0; i < banyak; i++) {
                TableColumn Kolom = Lihat.getColumnModel().getColumn(i);
                Kolom.setPreferredWidth(Lebar[i]);
                Lihat.setRowHeight(20);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "salah"+e);
        }
    }

    private void TampilModelTabelData(){
        try {
            String[]kolom={"Nama", "NIM", "Kode Buku", "Judul Buku", "Peminjaman", "Pengembalian", "Dikembalikan"};
            Dtm = new DefaultTableModel(null, kolom){
            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return false;
            }
        };
        TabelData.setModel(Dtm);
        AturJTable(TabelData, new int []{100,80,50,150,100,100,100} );
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "salah"+e);
        }
    }
    
      void TampilPadaTabel(){
        try {
            Con=Konek.getKoneksiDatabase();
            Stm = Con.createStatement(); 
            TampilModelTabelData();
            try{ 
                Sql = "select * from Peminjaman";
                Rs = Stm.executeQuery(Sql);
                while(Rs.next()){
                    Dtm.addRow(new Object[]{
                        Rs.getString("NIM"),
                        Rs.getString("Nama"),
                        Rs.getString("Kode_Buku"),
                        Rs.getString("Judul_Buku"),
                        Rs.getString("Peminjaman"),
                        Rs.getString("Pengembalian"),
                        Rs.getString("Dikembalikan")
                    });
                    TabelData.setModel(Dtm);
                }
            }catch(SQLException e){
            System.out.println("Ada Kesalahan " + e.toString());
            }
        }catch (SQLException e){
            System.out.println("koneksi gagal " + e.toString());
        }
 }
    
