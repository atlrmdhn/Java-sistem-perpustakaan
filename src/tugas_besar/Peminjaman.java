    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            SimpleDateFormat sdf1= new SimpleDateFormat("yyyy-MM-dd");
            String formattedDate1 = sdf1.format(chsPinjam.getDate());
            
            SimpleDateFormat sdf2 = new SimpleDateFormat("yyyy-MM-dd");
            String formattedDate2 = sdf2.format(chsKembalian.getDate());
            
            SimpleDateFormat sdf3 = new SimpleDateFormat("yyyy-MM-dd");
            String formattedDate3 = sdf3.format(chsKembalikan.getDate());
            
            Con = Konek.getKoneksiDatabase();
            Stm = null;
            Sql= "insert into peminjaman (NIM, Nama, Kode_Buku, Judul_Buku, Peminjaman, Pengembalian, Dikembalikan) values ('" 
            + txtNIM.getText() + "', '"
            + txtNama.getText() + "', '"
            + txtKdBuku.getText() + "', '"
            + txtJudul.getText() + "', '"
            + formattedDate1 + "', '"
            + formattedDate2 + "', '"
            + formattedDate3 +"')";
            Stm= Con.createStatement();
            int AdaPenambahanRecord= Stm.executeUpdate(Sql);
            if (AdaPenambahanRecord>0){
                JOptionPane.showMessageDialog(this,"Data Berhasil Tersimpan", "Informasi",JOptionPane.INFORMATION_MESSAGE);
                TampilPadaTabel();
            }
            else
            JOptionPane.showMessageDialog(this,"Data Gagal Tersimpan", "Informasi",JOptionPane.INFORMATION_MESSAGE);
            Stm.close();

        } catch (SQLException e){
            System.out.println("Koneksi Gagal " +e.toString());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

